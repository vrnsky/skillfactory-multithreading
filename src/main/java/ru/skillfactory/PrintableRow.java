package ru.skillfactory;

/**
 * @author Egor Voronianskii
 * @version 1.0
 */
public class PrintableRow {

    private int words;

    private int spaces;

    public PrintableRow() {
    }

    public int getWords() {
        return words;
    }

    public void setWords(int words) {
        this.words = words;
    }

    public int getSpaces() {
        return spaces;
    }

    public void setSpaces(int spaces) {
        this.spaces = spaces;
    }
}
